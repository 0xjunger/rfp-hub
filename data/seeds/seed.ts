async function seed() {
  console.log('Seeding RFP Hub database...\n');

  // 1. Clear existing data (opportunities first due to FK)
  console.log('Clearing existing data...');
  await db.delete(fundingOpportunities);
  await db.delete(fundingSources);

  // 2. Insert funding sources
  console.log('Inserting funding sources...');
  const insertedSources = await db.insert(fundingSources).values(sources).returning();
  console.log(`  ✓ ${insertedSources.length} funding sources inserted`);

  // Build slug→id lookup
  const sourceIdBySlug = new Map(insertedSources.map((s) => [s.slug, s.id]));

  // 3. Insert funding opportunities
  console.log('Inserting funding opportunities...');
  const oppValues = opportunities.map(({ _sourceSlug, ...opp }) => ({
    ...opp,
    sourceId: sourceIdBySlug.get(_sourceSlug)!,
  }));

  const insertedOpps = await db.insert(fundingOpportunities).values(oppValues).returning();
  console.log(`  ✓ ${insertedOpps.length} funding opportunities inserted`);

  // 4. Summary
  console.log('\nSeed complete!');
  console.log(`  Sources: ${insertedSources.length}`);
  console.log(`  Opportunities: ${insertedOpps.length}`);

  const statusCounts = insertedOpps.reduce(
    (acc, o) => {
      acc[o.status] = (acc[o.status] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );
  console.log('  By status:', statusCounts);

  const typeCounts = insertedOpps.reduce(
    (acc, o) => {
      acc[o.rfpType] = (acc[o.rfpType] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );
  console.log('  By type:', typeCounts);

  await client.end();
}

seed().catch((err) => {
  console.error('Seed failed:', err);
  process.exit(1);
});
